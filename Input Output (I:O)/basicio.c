#include <stdio.h>
int main()
{
    //variables
    int Integer = 5;
    float Float = 13.5;
    double Double = 12.4;
    char chr = 'a';

    //print statements
    printf("An integer = %d\n", Integer);
    printf("A Float = %f\n", Float);
    printf("A Double = %lf\n", Double);
    printf("A character = %c.\n", chr);

    return 0;
}
